def check_and_send_notifications():
    """
    –ü—Ä–æ–≤–µ—Ä—è–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –Ω—É–∂–Ω—ã–µ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è
    –ó–∞–ø—É—Å–∫–∞–µ—Ç—Å—è –ø–æ —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—é (cron/scheduler)
    """
    try:
        logger.info("üîç –ó–∞–ø—É—Å–∫ –ø—Ä–æ–≤–µ—Ä–∫–∏ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–π...")
        
        # 1. –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π —Å –Ω–∏–∑–∫–∏–º –±–∞–ª–∞–Ω—Å–æ–º (< 100 —Ç–æ–∫–µ–Ω–æ–≤)
        cursor = db.conn.cursor()
        cursor.execute("""
            SELECT id, tokens, username
            FROM users
            WHERE tokens < 100 AND tokens > 0
        """)
        low_balance_users = cursor.fetchall()
        
        logger.info(f"–ù–∞–π–¥–µ–Ω–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π —Å –Ω–∏–∑–∫–∏–º –±–∞–ª–∞–Ω—Å–æ–º: {len(low_balance_users)}")
        
        for user in low_balance_users:
            user_id, tokens, username = user
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–µ –æ—Ç–ø—Ä–∞–≤–ª—è–ª–∏ –ª–∏ —É–∂–µ —Å–µ–≥–æ–¥–Ω—è
            # (–º–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å —Ç–∞–±–ª–∏—Ü—É –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–Ω—ã—Ö —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–π)
            send_low_balance_notification(user_id, tokens)
        
        # 2. –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–µ–∞–∫—Ç–∏–≤–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π (–Ω–µ –∑–∞—Ö–æ–¥–∏–ª–∏ 7+ –¥–Ω–µ–π)
        week_ago = datetime.now() - timedelta(days=7)
        
        cursor.execute("""
            SELECT id, created_at, username
            FROM users
            WHERE created_at < %s
        """, (week_ago,))
        inactive_users = cursor.fetchall()
        
        logger.info(f"–ù–∞–π–¥–µ–Ω–æ –Ω–µ–∞–∫—Ç–∏–≤–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π: {len(inactive_users)}")
        
        for user in inactive_users:
            user_id, created_at, username = user
            days_inactive = (datetime.now() - created_at).days
            
            # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Ä–µ–∞–∫—Ç–∏–≤–∞—Ü–∏—é —Ç–æ–ª—å–∫–æ —Ä–∞–∑ –≤ 30 –¥–Ω–µ–π
            if days_inactive >= 7 and days_inactive % 30 == 0:
                send_reactivation_notification(user_id, days_inactive)
        
        cursor.close()
        logger.info("‚úÖ –ü—Ä–æ–≤–µ—Ä–∫–∞ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–π –∑–∞–≤–µ—Ä—à–µ–Ω–∞")
        
    except Exception as e:
        logger.error(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–æ–≤–µ—Ä–∫–∏ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–π: {e}")


