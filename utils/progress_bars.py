def generate_gradient_progress_bar(progress, total_blocks=12, title="–ü–†–û–ì–†–ï–°–°"):
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –∫—Ä–∞—Å–∏–≤—ã–π –ø—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä —Å –≥—Ä–∞–¥–∏–µ–Ω—Ç–æ–º –æ—Ç –∫—Ä–∞—Å–Ω–æ–≥–æ –∫ –∑–µ–ª–µ–Ω–æ–º—É
    –ò—Å–ø–æ–ª—å–∑—É–µ—Ç 12 –∫—Ä—É–≥–æ–≤ –≤–º–µ—Å—Ç–æ –∫–≤–∞–¥—Ä–∞—Ç–æ–≤
    
    Args:
        progress: –ü—Ä–æ–≥—Ä–µ—Å—Å –≤ –ø—Ä–æ—Ü–µ–Ω—Ç–∞—Ö (0-100)
        total_blocks: –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫—Ä—É–≥–æ–≤ –≤ –ø—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä–µ (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 12)
        title: –ó–∞–≥–æ–ª–æ–≤–æ–∫ –ø—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä–∞
    
    Returns:
        str: –û—Ç—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –ø—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä —Å –≥—Ä–∞–¥–∏–µ–Ω—Ç–æ–º –∏–∑ –∫—Ä—É–≥–æ–≤
    """
    # –†–∞—Å—á–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –∑–∞–ø–æ–ª–Ω–µ–Ω–Ω—ã—Ö –∫—Ä—É–≥–æ–≤
    filled_blocks = int((progress / 100) * total_blocks)
    
    # –ò—Å–ø–æ–ª—å–∑—É–µ–º –≥—Ä–∞–¥–∏–µ–Ω—Ç –∏–∑ 4 —Ü–≤–µ—Ç–æ–≤
    # üî¥ = –∫—Ä–∞—Å–Ω—ã–π (0-25%)
    # üü† = –æ—Ä–∞–Ω–∂–µ–≤—ã–π (25-50%)
    # üü° = –∂–µ–ª—Ç—ã–π (50-75%)
    # üü¢ = –∑–µ–ª–µ–Ω—ã–π (75-100%)
    # ‚ö™ = –±–µ–ª—ã–π (–ø—É—Å—Ç–æ–π)
    
    gradient_blocks = []
    for i in range(total_blocks):
        if i < filled_blocks:
            # –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Ü–≤–µ—Ç –∫—Ä—É–≥–∞ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç –ø–æ–∑–∏—Ü–∏–∏ (–ø–æ 3 –∫—Ä—É–≥–∞ –∫–∞–∂–¥–æ–≥–æ —Ü–≤–µ—Ç–∞)
            if i < 3:  # –ü–µ—Ä–≤—ã–µ 3 –∫—Ä—É–≥–∞ - –∫—Ä–∞—Å–Ω—ã–µ
                gradient_blocks.append("üî¥")
            elif i < 6:  # 4-6 –∫—Ä—É–≥–∏ - –æ—Ä–∞–Ω–∂–µ–≤—ã–µ
                gradient_blocks.append("üü†")
            elif i < 9:  # 7-9 –∫—Ä—É–≥–∏ - –∂–µ–ª—Ç—ã–µ
                gradient_blocks.append("üü°")
            else:  # 10-12 –∫—Ä—É–≥–∏ - –∑–µ–ª–µ–Ω—ã–µ
                gradient_blocks.append("üü¢")
        else:
            gradient_blocks.append("‚ö™")
    
    # –§–æ—Ä–º–∏—Ä—É–µ–º —Å—Ç—Ä–æ–∫—É –ë–ï–ó –ø—Ä–æ–±–µ–ª–æ–≤ - —Ç–∞–∫ Telegram –Ω–µ –æ–±—Ä–µ–∂–µ—Ç
    bar = "".join(gradient_blocks)
    
    return f"<b>{title}</b> {progress}%\n{bar}"


def generate_seo_score_bar(score):
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –ø—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä –¥–ª—è SEO –æ—Ü–µ–Ω–∫–∏ (0-100)
    –ò—Å–ø–æ–ª—å–∑—É–µ—Ç 12 –∫—Ä—É–≥–æ–≤
    
    Args:
        score: SEO –æ—Ü–µ–Ω–∫–∞ (0-100)
    
    Returns:
        str: –û—Ç—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –ø—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä —Å –æ—Ü–µ–Ω–∫–æ–π
    """
    filled_blocks = int((score / 100) * 12)
    
    gradient_blocks = []
    for i in range(12):
        if i < filled_blocks:
            block_score = ((i + 1) / 12) * 100
            
            if block_score <= 40:
                gradient_blocks.append("üî¥")
            elif block_score <= 70:
                gradient_blocks.append("üü°")
            else:
                gradient_blocks.append("üü¢")
        else:
            gradient_blocks.append("‚ö™")
    
    bar = "".join(gradient_blocks)  # –ë–ï–ó –ø—Ä–æ–±–µ–ª–æ–≤
    
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Å—Ç–∞—Ç—É—Å
    if score < 40:
        status_emoji = "üî¥"
        status_text = "–ü–ª–æ—Ö–æ"
    elif score < 70:
        status_emoji = "üü°"
        status_text = "–°—Ä–µ–¥–Ω–µ"
    elif score < 85:
        status_emoji = "üü¢"
        status_text = "–•–æ—Ä–æ—à–æ"
    else:
        status_emoji = "‚úÖ"
        status_text = "–û—Ç–ª–∏—á–Ω–æ"
    
    return f"{bar}\n{status_emoji} *{score}/100* ‚Äî _{status_text}_"